CREATE VIEW vw_calificacion_perfil_curso AS
SELECT *
FROM (
    SELECT
        c.curid,
        c.pleid,
        c.graid,
        g.nedid,
        p.pcaid,
        p.pcanombre,
        p.pcatipo_base,
        p.pcaestado,
        a.pasid,
        a.pasalcance,
        a.pasprioridad,
        ROW_NUMBER() OVER (
            PARTITION BY c.curid
            ORDER BY
                CASE a.pasalcance
                    WHEN 'CURSO' THEN 3
                    WHEN 'GRADO' THEN 2
                    WHEN 'NIVEL' THEN 1
                    ELSE 0
                END DESC,
                a.pasprioridad DESC,
                a.pasid DESC
        ) AS prioridad_resuelta
    FROM curso c
    INNER JOIN grado g ON g.graid = c.graid
    INNER JOIN perfil_calificacion_asignacion a
        ON a.pleid = c.pleid
        AND a.pasestado = true
        AND (
            (a.pasalcance = 'CURSO' AND a.curid = c.curid)
            OR
            (a.pasalcance = 'GRADO' AND a.graid = c.graid)
            OR
            (a.pasalcance = 'NIVEL' AND a.nedid = g.nedid)
        )
    INNER JOIN perfil_calificacion p ON p.pcaid = a.pcaid
    WHERE p.pcaestado IN ('ACTIVA', 'BLOQUEADA')
) perfiles
WHERE prioridad_resuelta = 1;

CREATE VIEW vw_calificacion_perfil_materia AS
SELECT *
FROM (
    SELECT
        mc.mtcid,
        mc.curid,
        c.pleid,
        c.graid,
        g.nedid,
        mc.asgid,
        p.pcaid,
        p.pcanombre,
        p.pcatipo_base,
        p.pcaestado,
        a.pasid,
        a.pasalcance,
        a.pasprioridad,
        ROW_NUMBER() OVER (
            PARTITION BY mc.mtcid
            ORDER BY
                CASE a.pasalcance
                    WHEN 'MATERIA' THEN 4
                    WHEN 'CURSO' THEN 3
                    WHEN 'GRADO' THEN 2
                    WHEN 'NIVEL' THEN 1
                    ELSE 0
                END DESC,
                a.pasprioridad DESC,
                a.pasid DESC
        ) AS prioridad_resuelta
    FROM materia_curso mc
    INNER JOIN curso c ON c.curid = mc.curid
    INNER JOIN grado g ON g.graid = c.graid
    INNER JOIN perfil_calificacion_asignacion a
        ON a.pleid = c.pleid
        AND a.pasestado = true
        AND (
            (a.pasalcance = 'MATERIA' AND a.mtcid = mc.mtcid)
            OR
            (a.pasalcance = 'CURSO' AND a.curid = c.curid)
            OR
            (a.pasalcance = 'GRADO' AND a.graid = c.graid)
            OR
            (a.pasalcance = 'NIVEL' AND a.nedid = g.nedid)
        )
    INNER JOIN perfil_calificacion p ON p.pcaid = a.pcaid
    WHERE p.pcaestado IN ('ACTIVA', 'BLOQUEADA')
) perfiles
WHERE prioridad_resuelta = 1;

CREATE VIEW vw_calificacion_materia_config_efectiva AS
SELECT
    v.mtcid,
    v.curid,
    v.pleid,
    v.graid,
    v.nedid,
    v.asgid,
    v.pcaid,
    v.pcanombre,
    v.pcatipo_base,
    COALESCE(mcc.mcctipo_registro, v.pcatipo_base) AS tipo_registro,
    COALESCE(mcc.mcctipo_visualizacion, 'MIXTA') AS tipo_visualizacion,
    COALESCE(mcc.mccpromediable, true) AS promediable,
    COALESCE(mcc.mccvisible_libreta, true) AS visible_libreta,
    COALESCE(mcc.mccusa_equivalencia, true) AS usa_equivalencia,
    mcc.mccid
FROM vw_calificacion_perfil_materia v
LEFT JOIN materia_calificacion_config mcc
    ON mcc.mtcid = v.mtcid
    AND mcc.pcaid = v.pcaid
    AND mcc.mccestado = true;

