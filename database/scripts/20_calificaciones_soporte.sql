-- Soporte para configuracion de calificaciones.
--
-- Depende de database/scripts/18_calificaciones.sql y
-- database/scripts/19_calificaciones_promocion.sql. Agrega plantillas para
-- crear perfiles por periodo y vistas para resolver el perfil efectivo.

CREATE TABLE plantilla_calificacion (
    pclid integer GENERATED BY DEFAULT AS IDENTITY,
    pclnombre varchar(120) NOT NULL,
    pcldescripcion varchar(250),
    pcltipo_base varchar(30) NOT NULL DEFAULT 'CUANTITATIVO',
    pclminima numeric(5,2),
    pclmaxima numeric(5,2),
    pclaprobacion numeric(5,2),
    pcldecimales integer NOT NULL DEFAULT 2,
    pclmetodo_decimal varchar(20) NOT NULL DEFAULT 'REDONDEO',
    pclpromedia_final boolean NOT NULL DEFAULT true,
    pclaplica_promocion boolean NOT NULL DEFAULT true,
    pclestado boolean NOT NULL DEFAULT true,
    pclfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    pclfecha_modificacion timestamp,
    CONSTRAINT pk_plantilla_calificacion PRIMARY KEY (pclid),
    CONSTRAINT uq_plantilla_calificacion_nombre UNIQUE (pclnombre),
    CONSTRAINT ck_pcl_tipo_base
        CHECK (pcltipo_base IN ('CUANTITATIVO', 'CUALITATIVO', 'AMBITOS_DESTREZAS')),
    CONSTRAINT ck_pcl_escala
        CHECK (
            (
                pcltipo_base IN ('CUALITATIVO', 'AMBITOS_DESTREZAS')
                AND pclminima IS NULL
                AND pclmaxima IS NULL
                AND pclaprobacion IS NULL
            )
            OR
            (
                pcltipo_base = 'CUANTITATIVO'
                AND pclminima IS NOT NULL
                AND pclmaxima IS NOT NULL
                AND pclaprobacion IS NOT NULL
                AND pclminima < pclmaxima
                AND pclaprobacion BETWEEN pclminima AND pclmaxima
            )
        ),
    CONSTRAINT ck_pcl_decimales
        CHECK (pcldecimales BETWEEN 0 AND 4),
    CONSTRAINT ck_pcl_metodo_decimal
        CHECK (pclmetodo_decimal IN ('REDONDEO', 'TRUNCAMIENTO'))
);

CREATE TABLE plantilla_subperiodo (
    psuid integer GENERATED BY DEFAULT AS IDENTITY,
    pclid integer NOT NULL,
    psunombre varchar(80) NOT NULL,
    psuorden integer NOT NULL,
    psuparticipa_final boolean NOT NULL DEFAULT true,
    psupeso_final numeric(6,3),
    CONSTRAINT pk_plantilla_subperiodo PRIMARY KEY (psuid),
    CONSTRAINT fk_psu_plantilla FOREIGN KEY (pclid)
        REFERENCES plantilla_calificacion (pclid),
    CONSTRAINT uq_psu_plantilla_nombre UNIQUE (pclid, psunombre),
    CONSTRAINT uq_psu_plantilla_orden UNIQUE (pclid, psuorden),
    CONSTRAINT ck_psu_peso
        CHECK (psupeso_final IS NULL OR psupeso_final > 0)
);

CREATE TABLE plantilla_componente (
    pcoid integer GENERATED BY DEFAULT AS IDENTITY,
    psuid integer NOT NULL,
    pconombre varchar(100) NOT NULL,
    pcoorden integer NOT NULL,
    pcopeso numeric(6,3),
    pcotipo_calculo varchar(30) NOT NULL DEFAULT 'PROMEDIO_SIMPLE',
    pcoestado boolean NOT NULL DEFAULT true,
    CONSTRAINT pk_plantilla_componente PRIMARY KEY (pcoid),
    CONSTRAINT fk_pco_subperiodo FOREIGN KEY (psuid)
        REFERENCES plantilla_subperiodo (psuid),
    CONSTRAINT uq_pco_subperiodo_nombre UNIQUE (psuid, pconombre),
    CONSTRAINT uq_pco_subperiodo_orden UNIQUE (psuid, pcoorden),
    CONSTRAINT ck_pco_tipo_calculo
        CHECK (pcotipo_calculo IN ('PROMEDIO_SIMPLE', 'PROMEDIO_PONDERADO', 'SUMA')),
    CONSTRAINT ck_pco_peso
        CHECK (pcopeso IS NULL OR pcopeso > 0)
);

CREATE TABLE plantilla_escala_cualitativa (
    pecid integer GENERATED BY DEFAULT AS IDENTITY,
    pclid integer NOT NULL,
    peccodigo varchar(20) NOT NULL,
    pecnombre varchar(80) NOT NULL,
    pecdescripcion varchar(250),
    pecvalor_minimo numeric(5,2),
    pecvalor_maximo numeric(5,2),
    pecorden integer NOT NULL,
    pecestado boolean NOT NULL DEFAULT true,
    CONSTRAINT pk_plantilla_escala_cualitativa PRIMARY KEY (pecid),
    CONSTRAINT fk_pec_plantilla FOREIGN KEY (pclid)
        REFERENCES plantilla_calificacion (pclid),
    CONSTRAINT uq_pec_plantilla_codigo UNIQUE (pclid, peccodigo),
    CONSTRAINT uq_pec_plantilla_orden UNIQUE (pclid, pecorden),
    CONSTRAINT ck_pec_rango
        CHECK (
            (pecvalor_minimo IS NULL AND pecvalor_maximo IS NULL)
            OR
            (pecvalor_minimo IS NOT NULL AND pecvalor_maximo IS NOT NULL AND pecvalor_maximo >= pecvalor_minimo)
        )
);

