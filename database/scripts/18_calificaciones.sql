    usuid_modificacion integer,
    cesfecha_modificacion timestamp,
    CONSTRAINT pk_calificacion_estudiante PRIMARY KEY (cesid),
    CONSTRAINT fk_ces_actividad FOREIGN KEY (aciid)
        REFERENCES actividad_calificacion (aciid),
    CONSTRAINT fk_ces_matricula FOREIGN KEY (matid)
        REFERENCES matricula (matid),
    CONSTRAINT fk_ces_escala FOREIGN KEY (ecaid)
