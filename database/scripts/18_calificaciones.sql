-- Modulo de calificaciones.
--
-- Este script inicia la estructura del motor de evaluacion definido en
-- docs/logica-calificaciones.md. La configuracion vive por periodo lectivo y
-- perfil; los resultados se guardan como snapshots calculados para reportes.

CREATE TABLE perfil_calificacion (
    pcaid integer GENERATED BY DEFAULT AS IDENTITY,
    pleid integer NOT NULL,
    pcaid_origen integer,
    pcanombre varchar(120) NOT NULL,
    pcadescripcion varchar(250),
    pcaversion integer NOT NULL DEFAULT 1,
    pcaestado varchar(20) NOT NULL DEFAULT 'BORRADOR',
    pcatipo_base varchar(30) NOT NULL DEFAULT 'CUANTITATIVO',
    pcavigencia_desde date NOT NULL,
    pcavigencia_hasta date,
    pcaminima numeric(5,2),
    pcamaxima numeric(5,2),
    pcaaprobacion numeric(5,2),
    pcadecimales integer NOT NULL DEFAULT 2,
    pcametodo_decimal varchar(20) NOT NULL DEFAULT 'REDONDEO',
    pcapromedia_final boolean NOT NULL DEFAULT true,
    pcaaplica_promocion boolean NOT NULL DEFAULT true,
    pcamotivo_version varchar(250),
    usuid_creacion integer NOT NULL,
    usuid_autorizacion integer,
    pcafecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    pcafecha_modificacion timestamp,
    CONSTRAINT pk_perfil_calificacion PRIMARY KEY (pcaid),
    CONSTRAINT fk_pca_periodo FOREIGN KEY (pleid)
        REFERENCES periodo_lectivo (pleid),
    CONSTRAINT fk_pca_origen FOREIGN KEY (pcaid_origen)
        REFERENCES perfil_calificacion (pcaid),
    CONSTRAINT fk_pca_usuario_creacion FOREIGN KEY (usuid_creacion)
        REFERENCES usuario (usuid),
    CONSTRAINT fk_pca_usuario_autorizacion FOREIGN KEY (usuid_autorizacion)
        REFERENCES usuario (usuid),
    CONSTRAINT uq_pca_id_periodo UNIQUE (pcaid, pleid),
    CONSTRAINT uq_pca_periodo_nombre_version UNIQUE (pleid, pcanombre, pcaversion),
    CONSTRAINT ck_pca_estado
        CHECK (pcaestado IN ('BORRADOR', 'ACTIVA', 'EN_REVISION', 'BLOQUEADA', 'ARCHIVADA')),
    CONSTRAINT ck_pca_tipo_base
        CHECK (pcatipo_base IN ('CUANTITATIVO', 'CUALITATIVO', 'AMBITOS_DESTREZAS')),
    CONSTRAINT ck_pca_fechas
        CHECK (pcavigencia_hasta IS NULL OR pcavigencia_hasta >= pcavigencia_desde),
    CONSTRAINT ck_pca_escala
        CHECK (
            (
                pcatipo_base IN ('CUALITATIVO', 'AMBITOS_DESTREZAS')
                AND pcaminima IS NULL
                AND pcamaxima IS NULL
                AND pcaaprobacion IS NULL
            )
            OR
            (
                pcatipo_base = 'CUANTITATIVO'
                AND pcaminima IS NOT NULL
                AND pcamaxima IS NOT NULL
                AND pcaaprobacion IS NOT NULL
                AND pcaminima < pcamaxima
                AND pcaaprobacion BETWEEN pcaminima AND pcamaxima
            )
        ),
    CONSTRAINT ck_pca_decimales
        CHECK (pcadecimales BETWEEN 0 AND 4),
    CONSTRAINT ck_pca_metodo_decimal
        CHECK (pcametodo_decimal IN ('REDONDEO', 'TRUNCAMIENTO'))
);

CREATE TABLE perfil_calificacion_asignacion (
    pasid integer GENERATED BY DEFAULT AS IDENTITY,
    pcaid integer NOT NULL,
    pleid integer NOT NULL,
    pasalcance varchar(20) NOT NULL,
    nedid integer,
    graid integer,
    curid integer,
    mtcid integer,
    pasprioridad integer NOT NULL,
    pasestado boolean NOT NULL DEFAULT true,
    pasobservacion varchar(250),
    usuid_registro integer NOT NULL,
    pasfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    pasfecha_modificacion timestamp,
    CONSTRAINT pk_perfil_calificacion_asignacion PRIMARY KEY (pasid),
    CONSTRAINT fk_pas_perfil FOREIGN KEY (pcaid)
        REFERENCES perfil_calificacion (pcaid),
    CONSTRAINT fk_pas_perfil_periodo FOREIGN KEY (pcaid, pleid)
        REFERENCES perfil_calificacion (pcaid, pleid),
    CONSTRAINT fk_pas_periodo FOREIGN KEY (pleid)
        REFERENCES periodo_lectivo (pleid),
    CONSTRAINT fk_pas_nivel FOREIGN KEY (nedid)
        REFERENCES nivel_educativo (nedid),
    CONSTRAINT fk_pas_grado FOREIGN KEY (graid)
        REFERENCES grado (graid),
    CONSTRAINT fk_pas_curso FOREIGN KEY (curid)
        REFERENCES curso (curid),
    CONSTRAINT fk_pas_materia FOREIGN KEY (mtcid)
        REFERENCES materia_curso (mtcid),
    CONSTRAINT fk_pas_usuario FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_pas_alcance
        CHECK (pasalcance IN ('NIVEL', 'GRADO', 'CURSO', 'MATERIA')),
    CONSTRAINT ck_pas_objetivo
        CHECK (
            (pasalcance = 'NIVEL' AND nedid IS NOT NULL AND graid IS NULL AND curid IS NULL AND mtcid IS NULL)
            OR
            (pasalcance = 'GRADO' AND nedid IS NULL AND graid IS NOT NULL AND curid IS NULL AND mtcid IS NULL)
            OR
            (pasalcance = 'CURSO' AND nedid IS NULL AND graid IS NULL AND curid IS NOT NULL AND mtcid IS NULL)
            OR
            (pasalcance = 'MATERIA' AND nedid IS NULL AND graid IS NULL AND curid IS NULL AND mtcid IS NOT NULL)
        ),
    CONSTRAINT ck_pas_prioridad
        CHECK (pasprioridad > 0)
);

CREATE UNIQUE INDEX uq_pas_nivel_activo
    ON perfil_calificacion_asignacion (pleid, nedid)
    WHERE pasestado = true AND pasalcance = 'NIVEL';

CREATE UNIQUE INDEX uq_pas_grado_activo
    ON perfil_calificacion_asignacion (pleid, graid)
    WHERE pasestado = true AND pasalcance = 'GRADO';

CREATE UNIQUE INDEX uq_pas_curso_activo
    ON perfil_calificacion_asignacion (pleid, curid)
    WHERE pasestado = true AND pasalcance = 'CURSO';

CREATE UNIQUE INDEX uq_pas_materia_activo
    ON perfil_calificacion_asignacion (pleid, mtcid)
    WHERE pasestado = true AND pasalcance = 'MATERIA';

CREATE TABLE subperiodo_calificacion (
    spcid integer GENERATED BY DEFAULT AS IDENTITY,
    pcaid integer NOT NULL,
    spcnombre varchar(80) NOT NULL,
    spcorden integer NOT NULL,
    spcfecha_inicio date NOT NULL,
    spcfecha_fin date NOT NULL,
    spcestado varchar(20) NOT NULL DEFAULT 'EN_REGISTRO',
    spcparticipa_final boolean NOT NULL DEFAULT true,
    spcpeso_final numeric(6,3),
    spcfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    spcfecha_modificacion timestamp,
    CONSTRAINT pk_subperiodo_calificacion PRIMARY KEY (spcid),
    CONSTRAINT fk_spc_perfil FOREIGN KEY (pcaid)
        REFERENCES perfil_calificacion (pcaid),
    CONSTRAINT uq_spc_perfil_nombre UNIQUE (pcaid, spcnombre),
    CONSTRAINT uq_spc_perfil_orden UNIQUE (pcaid, spcorden),
    CONSTRAINT ck_spc_fechas
        CHECK (spcfecha_fin >= spcfecha_inicio),
    CONSTRAINT ck_spc_estado
        CHECK (spcestado IN ('EN_REGISTRO', 'CERRADO_DOCENTE', 'VALIDADO_COORDINACION', 'PUBLICADO')),
    CONSTRAINT ck_spc_peso
        CHECK (spcpeso_final IS NULL OR spcpeso_final > 0)
);

