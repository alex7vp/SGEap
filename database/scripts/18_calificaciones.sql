CREATE TABLE escala_cualitativa (
    ecaid integer GENERATED BY DEFAULT AS IDENTITY,
    pcaid integer NOT NULL,
    ecacodigo varchar(20) NOT NULL,
    ecanombre varchar(80) NOT NULL,
    ecadescripcion varchar(250),
    ecavalor_minimo numeric(5,2),
    ecavalor_maximo numeric(5,2),
    ecaorden integer NOT NULL,
    ecaestado boolean NOT NULL DEFAULT true,
    CONSTRAINT pk_escala_cualitativa PRIMARY KEY (ecaid),
    CONSTRAINT fk_eca_perfil FOREIGN KEY (pcaid)
        REFERENCES perfil_calificacion (pcaid),
    CONSTRAINT uq_eca_perfil_codigo UNIQUE (pcaid, ecacodigo),
    CONSTRAINT uq_eca_perfil_orden UNIQUE (pcaid, ecaorden),
    CONSTRAINT ck_eca_rango
        CHECK (
            (ecavalor_minimo IS NULL AND ecavalor_maximo IS NULL)
            OR
            (ecavalor_minimo IS NOT NULL AND ecavalor_maximo IS NOT NULL AND ecavalor_maximo >= ecavalor_minimo)
        )
);

