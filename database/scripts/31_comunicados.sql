CREATE INDEX idx_comunicado_estado_fecha
    ON comunicado (comestado, comfecha_publicacion DESC);

CREATE INDEX idx_comunicado_destinatario_usuario_lectura
    ON comunicado_destinatario (usuid, cdeestado_lectura);

