CREATE TABLE contabilidad_pago (
    cpagid integer GENERATED BY DEFAULT AS IDENTITY,
    matid integer NOT NULL,
    cobid_sugerido integer,
    creid_sugerido integer,
    cpagorigen varchar(20) NOT NULL,
    cpagestado varchar(20) NOT NULL DEFAULT 'EN_REVISION',
    cpagvalor_reportado numeric(10,2),
    cpagvalor_aprobado numeric(10,2),
    cmpid integer,
    cpagreferencia varchar(80),
    cpagarchivo_ruta varchar(255),
    cpagarchivo_nombre varchar(180),
    cpagarchivo_extension varchar(10),
    cpagarchivo_mime varchar(100),
    cpagarchivo_tamano integer,
    cpagarchivo_hash varchar(128),
    cpagobservacion_interna varchar(250),
    cpagmotivo_rechazo varchar(250),
    cpagdocumento_externo_sistema varchar(50),
    cpagdocumento_externo_numero varchar(80),
    cpagdocumento_externo_fecha date,
    usuid_registro integer NOT NULL,
    cpagfecha_registro timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    usuid_revision integer,
    cpagfecha_revision timestamp,
    usuid_reverso integer,
    cpagfecha_reverso timestamp,
    cpagmotivo_reverso varchar(250),
    CONSTRAINT pk_contabilidad_pago PRIMARY KEY (cpagid),
    CONSTRAINT fk_cpag_matricula FOREIGN KEY (matid)
        REFERENCES matricula (matid),
    CONSTRAINT fk_cpag_obligacion_sugerida FOREIGN KEY (cobid_sugerido)
        REFERENCES contabilidad_obligacion (cobid),
    CONSTRAINT fk_cpag_rubro_sugerido FOREIGN KEY (creid_sugerido)
        REFERENCES contabilidad_rubro_estudiante (creid),
    CONSTRAINT fk_cpag_metodo FOREIGN KEY (cmpid)
        REFERENCES contabilidad_metodo_pago (cmpid),
    CONSTRAINT fk_cpag_usuario_registro FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT fk_cpag_usuario_revision FOREIGN KEY (usuid_revision)
        REFERENCES usuario (usuid),
    CONSTRAINT fk_cpag_usuario_reverso FOREIGN KEY (usuid_reverso)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_cpag_origen
        CHECK (cpagorigen IN ('REPRESENTANTE', 'INTERNO')),
    CONSTRAINT ck_cpag_estado
        CHECK (cpagestado IN ('EN_REVISION', 'APROBADO', 'RECHAZADO', 'ANULADO', 'REVERSADO')),
    CONSTRAINT ck_cpag_valores
        CHECK (
            (cpagvalor_reportado IS NULL OR cpagvalor_reportado >= 0)
            AND (cpagvalor_aprobado IS NULL OR cpagvalor_aprobado >= 0)
        ),
    CONSTRAINT ck_cpag_archivo_tamano
        CHECK (cpagarchivo_tamano IS NULL OR cpagarchivo_tamano BETWEEN 1 AND 2097152),
    CONSTRAINT ck_cpag_archivo_extension
        CHECK (
            cpagarchivo_extension IS NULL
            OR cpagarchivo_extension IN ('PDF', 'JPG', 'JPEG', 'PNG')
        ),
    CONSTRAINT ck_cpag_representante_objetivo
        CHECK (
            cpagorigen <> 'REPRESENTANTE'
            OR (
                cobid_sugerido IS NOT NULL
                AND creid_sugerido IS NULL
                AND cpagarchivo_ruta IS NOT NULL
                AND cpagvalor_reportado IS NOT NULL
            )
        ),
    CONSTRAINT ck_cpag_revision
        CHECK (
            cpagestado = 'EN_REVISION'
            OR (
                usuid_revision IS NOT NULL
                AND cpagfecha_revision IS NOT NULL
            )
        ),
    CONSTRAINT ck_cpag_rechazo
        CHECK (
            cpagestado <> 'RECHAZADO'
            OR NULLIF(trim(cpagmotivo_rechazo), '') IS NOT NULL
        ),
    CONSTRAINT ck_cpag_aprobacion
        CHECK (
            cpagestado <> 'APROBADO'
            OR cpagvalor_aprobado IS NOT NULL
        ),
    CONSTRAINT ck_cpag_reverso
        CHECK (
            cpagestado <> 'REVERSADO'
            OR (
                usuid_reverso IS NOT NULL
                AND cpagfecha_reverso IS NOT NULL
                AND NULLIF(trim(cpagmotivo_reverso), '') IS NOT NULL
            )
        )
);

