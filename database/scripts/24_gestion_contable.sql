-- Modulo de Gestion Contable.
--
-- Administra obligaciones escolares, rubros adicionales, comprobantes de pago,
-- saldos internos y auditoria. No emite facturas ni se acopla a proveedores
-- externos de facturacion.

CREATE TABLE contabilidad_concepto (
    ccoid integer GENERATED BY DEFAULT AS IDENTITY,
    ccocodigo varchar(40) NOT NULL,
    cconombre varchar(120) NOT NULL,
    ccocategoria varchar(20) NOT NULL,
    ccodescripcion varchar(250),
    ccoestado boolean NOT NULL DEFAULT true,
    ccofecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    ccofecha_modificacion timestamp,
    CONSTRAINT pk_contabilidad_concepto PRIMARY KEY (ccoid),
    CONSTRAINT uq_contabilidad_concepto_categoria_codigo UNIQUE (ccocategoria, ccocodigo),
    CONSTRAINT uq_contabilidad_concepto_categoria_nombre UNIQUE (ccocategoria, cconombre),
    CONSTRAINT ck_contabilidad_concepto_categoria
        CHECK (ccocategoria IN ('OBLIGACION', 'RUBRO'))
);

CREATE TABLE contabilidad_metodo_pago (
    cmpid integer GENERATED BY DEFAULT AS IDENTITY,
    cmpcodigo varchar(30) NOT NULL,
    cmpnombre varchar(80) NOT NULL,
    cmpdescripcion varchar(200),
    cmpestado boolean NOT NULL DEFAULT true,
    cmpfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cmpfecha_modificacion timestamp,
    CONSTRAINT pk_contabilidad_metodo_pago PRIMARY KEY (cmpid),
    CONSTRAINT uq_contabilidad_metodo_pago_codigo UNIQUE (cmpcodigo),
    CONSTRAINT uq_contabilidad_metodo_pago_nombre UNIQUE (cmpnombre)
);

CREATE TABLE contabilidad_configuracion_obligacion (
    cfoid integer GENERATED BY DEFAULT AS IDENTITY,
    pleid integer NOT NULL,
    cfoalcance varchar(20) NOT NULL DEFAULT 'NIVEL',
    nedid integer,
    graid integer,
    ccoid integer NOT NULL,
    cfotipo varchar(20) NOT NULL,
    cfovalor_oficial numeric(10,2) NOT NULL,
    cfocantidad_pensiones integer,
    cfomes_inicio integer,
    cfomes_fin integer,
    cfoanio_inicio integer,
    cfodia_vencimiento integer NOT NULL DEFAULT 5,
    cfogenera_mora boolean NOT NULL DEFAULT false,
    cfomora_tipo varchar(20),
    cfomora_valor numeric(10,2),
    cfoestado boolean NOT NULL DEFAULT true,
    cfoobservacion varchar(250),
    usuid_registro integer NOT NULL,
    cfofecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cfofecha_modificacion timestamp,
    CONSTRAINT pk_contabilidad_configuracion_obligacion PRIMARY KEY (cfoid),
    CONSTRAINT fk_cfo_periodo FOREIGN KEY (pleid)
        REFERENCES periodo_lectivo (pleid),
    CONSTRAINT fk_cfo_nivel FOREIGN KEY (nedid)
        REFERENCES nivel_educativo (nedid),
    CONSTRAINT fk_cfo_grado FOREIGN KEY (graid)
        REFERENCES grado (graid),
    CONSTRAINT fk_cfo_concepto FOREIGN KEY (ccoid)
        REFERENCES contabilidad_concepto (ccoid),
    CONSTRAINT fk_cfo_usuario FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_cfo_alcance
        CHECK (cfoalcance IN ('INSTITUCION', 'NIVEL', 'GRADO')),
    CONSTRAINT ck_cfo_tipo
        CHECK (cfotipo IN ('MATRICULA', 'PENSION')),
    CONSTRAINT ck_cfo_objetivo
        CHECK (
            (cfoalcance = 'INSTITUCION' AND nedid IS NULL AND graid IS NULL)
            OR
            (cfoalcance = 'NIVEL' AND nedid IS NOT NULL AND graid IS NULL)
            OR
            (cfoalcance = 'GRADO' AND nedid IS NULL AND graid IS NOT NULL)
        ),
    CONSTRAINT ck_cfo_valor_oficial
        CHECK (cfovalor_oficial >= 0),
    CONSTRAINT ck_cfo_cantidad_pensiones
        CHECK (cfocantidad_pensiones IS NULL OR cfocantidad_pensiones > 0),
    CONSTRAINT ck_cfo_mes_inicio
        CHECK (cfomes_inicio IS NULL OR cfomes_inicio BETWEEN 1 AND 12),
    CONSTRAINT ck_cfo_mes_fin
        CHECK (cfomes_fin IS NULL OR cfomes_fin BETWEEN 1 AND 12),
    CONSTRAINT ck_cfo_dia_vencimiento
        CHECK (cfodia_vencimiento BETWEEN 1 AND 28),
    CONSTRAINT ck_cfo_mora_tipo
        CHECK (cfomora_tipo IS NULL OR cfomora_tipo IN ('VALOR_FIJO', 'PORCENTAJE')),
    CONSTRAINT ck_cfo_mora_valor
        CHECK (cfomora_valor IS NULL OR cfomora_valor >= 0),
    CONSTRAINT ck_cfo_pension_config
        CHECK (
            (
                cfotipo = 'PENSION'
                AND cfocantidad_pensiones IS NOT NULL
                AND cfomes_inicio IS NOT NULL
                AND cfomes_fin IS NOT NULL
                AND cfoanio_inicio IS NOT NULL
            )
            OR
            (
                cfotipo = 'MATRICULA'
                AND cfocantidad_pensiones IS NULL
                AND cfomes_inicio IS NULL
                AND cfomes_fin IS NULL
                AND cfoanio_inicio IS NULL
            )
        ),
    CONSTRAINT ck_cfo_mora_config
        CHECK (
            (
                cfogenera_mora = false
                AND cfomora_tipo IS NULL
                AND cfomora_valor IS NULL
            )
            OR
            (
                cfogenera_mora = true
                AND cfomora_tipo IS NOT NULL
                AND cfomora_valor IS NOT NULL
            )
        )
);

