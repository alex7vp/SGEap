    csfid integer NOT NULL,
    cobid integer,
    csmvalor numeric(10,2) NOT NULL,
    csmtipo varchar(20) NOT NULL,
    csmobservacion varchar(250),
    usuid_registro integer NOT NULL,
    csmfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_contabilidad_saldo_movimiento PRIMARY KEY (csmid),
    CONSTRAINT fk_csm_saldo FOREIGN KEY (csfid)
        REFERENCES contabilidad_saldo_favor (csfid),
    CONSTRAINT fk_csm_obligacion FOREIGN KEY (cobid)
        REFERENCES contabilidad_obligacion (cobid),
    CONSTRAINT fk_csm_usuario FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_csm_valor
        CHECK (csmvalor > 0),
    CONSTRAINT ck_csm_tipo
        CHECK (csmtipo IN ('APLICACION', 'REVERSO'))
);

CREATE TABLE contabilidad_auditoria (
    cauid integer GENERATED BY DEFAULT AS IDENTITY,
    cautabla varchar(80) NOT NULL,
    cauregistro_id integer NOT NULL,
    cauaccion varchar(40) NOT NULL,
    cauvalor_anterior text,
    cauvalor_nuevo text,
    cauobservacion varchar(250),
    usuid integer NOT NULL,
    caufecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_contabilidad_auditoria PRIMARY KEY (cauid),
    CONSTRAINT fk_cau_usuario FOREIGN KEY (usuid)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_cau_accion
        CHECK (
            cauaccion IN (
                'CREAR',
                'EDITAR',
                'ANULAR',
                'APROBAR',
                'RECHAZAR',
                'REVERSAR',
                'DOCUMENTO_EXTERNO',
                'DUPLICADO_ACEPTADO'
            )
        )
);

CREATE INDEX ix_cau_entidad
    ON contabilidad_auditoria (cautabla, cauregistro_id);

CREATE INDEX ix_cau_fecha
    ON contabilidad_auditoria (caufecha_creacion);
