CREATE TABLE contabilidad_obligacion (
    cobid integer GENERATED BY DEFAULT AS IDENTITY,
    matid integer NOT NULL,
    ccoid integer NOT NULL,
    cfoid integer,
    cobtipo varchar(20) NOT NULL,
    cobdescripcion varchar(150) NOT NULL,
    cobanio integer,
    cobmes integer,
    coborden integer NOT NULL DEFAULT 1,
    cobfecha_vencimiento date,
    cobvalor_base numeric(10,2) NOT NULL,
    cobdescuento_tipo varchar(20),
    cobdescuento_valor numeric(10,2),
    cobvalor_descuento numeric(10,2) NOT NULL DEFAULT 0,
    cobvalor_final numeric(10,2) NOT NULL,
    cobvalor_pagado numeric(10,2) NOT NULL DEFAULT 0,
    cobsaldo_pendiente numeric(10,2) NOT NULL,
    cobmotivo_descuento varchar(250),
    cobgenera_mora boolean NOT NULL DEFAULT false,
    cobmora_tipo varchar(20),
    cobmora_valor numeric(10,2),
    cobestado varchar(20) NOT NULL DEFAULT 'PENDIENTE',
    cobobservacion_interna varchar(250),
    usuid_registro integer NOT NULL,
    usuid_anulacion integer,
    cobfecha_anulacion timestamp,
    cobmotivo_anulacion varchar(250),
    cobfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cobfecha_modificacion timestamp,
    CONSTRAINT pk_contabilidad_obligacion PRIMARY KEY (cobid),
    CONSTRAINT fk_cob_matricula FOREIGN KEY (matid)
        REFERENCES matricula (matid),
    CONSTRAINT fk_cob_concepto FOREIGN KEY (ccoid)
        REFERENCES contabilidad_concepto (ccoid),
    CONSTRAINT fk_cob_configuracion FOREIGN KEY (cfoid)
        REFERENCES contabilidad_configuracion_obligacion (cfoid),
    CONSTRAINT fk_cob_usuario_registro FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT fk_cob_usuario_anulacion FOREIGN KEY (usuid_anulacion)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_cob_tipo
        CHECK (cobtipo IN ('MATRICULA', 'PENSION')),
    CONSTRAINT ck_cob_mes
        CHECK (cobmes IS NULL OR cobmes BETWEEN 1 AND 12),
    CONSTRAINT ck_cob_valores
        CHECK (
            cobvalor_base >= 0
            AND cobvalor_descuento >= 0
            AND cobvalor_final >= 0
            AND cobvalor_pagado >= 0
            AND cobsaldo_pendiente >= 0
            AND cobvalor_descuento <= cobvalor_base
            AND cobvalor_final = cobvalor_base - cobvalor_descuento
            AND cobvalor_pagado <= cobvalor_final
            AND cobsaldo_pendiente = cobvalor_final - cobvalor_pagado
        ),
    CONSTRAINT ck_cob_descuento_tipo
        CHECK (cobdescuento_tipo IS NULL OR cobdescuento_tipo IN ('VALOR_FIJO', 'PORCENTAJE')),
    CONSTRAINT ck_cob_descuento_valor
        CHECK (cobdescuento_valor IS NULL OR cobdescuento_valor >= 0),
    CONSTRAINT ck_cob_mora_tipo
        CHECK (cobmora_tipo IS NULL OR cobmora_tipo IN ('VALOR_FIJO', 'PORCENTAJE')),
    CONSTRAINT ck_cob_mora_valor
        CHECK (cobmora_valor IS NULL OR cobmora_valor >= 0),
    CONSTRAINT ck_cob_estado
        CHECK (cobestado IN ('PENDIENTE', 'EN_REVISION', 'PAGO_PARCIAL', 'PAGADO', 'VENCIDO', 'ANULADO')),
    CONSTRAINT ck_cob_periodo_pension
        CHECK (
            (
                cobtipo = 'PENSION'
                AND cobanio IS NOT NULL
                AND cobmes IS NOT NULL
            )
            OR
            (
                cobtipo = 'MATRICULA'
                AND cobanio IS NULL
                AND cobmes IS NULL
            )
        ),
    CONSTRAINT ck_cob_anulacion
        CHECK (
            cobestado <> 'ANULADO'
            OR (
                usuid_anulacion IS NOT NULL
                AND cobfecha_anulacion IS NOT NULL
                AND NULLIF(trim(cobmotivo_anulacion), '') IS NOT NULL
            )
        )
);

CREATE UNIQUE INDEX uq_cob_matricula
    ON contabilidad_obligacion (matid)
    WHERE cobtipo = 'MATRICULA';

CREATE UNIQUE INDEX uq_cob_pension_mes
    ON contabilidad_obligacion (matid, cobanio, cobmes)
    WHERE cobtipo = 'PENSION';

CREATE INDEX ix_cob_matricula_estado
    ON contabilidad_obligacion (matid, cobestado);

CREATE INDEX ix_cob_vencimiento_estado
    ON contabilidad_obligacion (cobfecha_vencimiento, cobestado);

