    cpagfecha_registro timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    usuid_revision integer,
    cpagfecha_revision timestamp,
    usuid_reverso integer,
    cpagfecha_reverso timestamp,
    cpagmotivo_reverso varchar(250),
    CONSTRAINT pk_contabilidad_pago PRIMARY KEY (cpagid),
    CONSTRAINT fk_cpag_matricula FOREIGN KEY (matid)
        REFERENCES matricula (matid),
    CONSTRAINT fk_cpag_obligacion_sugerida FOREIGN KEY (cobid_sugerido)
        REFERENCES contabilidad_obligacion (cobid),
