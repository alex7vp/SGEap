CREATE VIEW vw_materia_curso AS
SELECT
    mc.mtcid,
    mc.curid,
    mc.asgid,
    c.pleid,
    c.graid,
    c.prlid,
    a.areaid,
    aa.areanombre,
    a.asgnombre,
    g.granombre,
    p.prlnombre,
    CASE
        WHEN paralelos.total_paralelos > 1
            THEN a.asgnombre || ' ' || g.granombre || p.prlnombre
        ELSE a.asgnombre || ' ' || g.granombre
    END AS mtcnombre_mostrar,
    mc.mtcfecha_inicio,
    mc.mtcfecha_fin,
    mc.mtcorden,
    mc.mtcestado
FROM materia_curso mc
INNER JOIN asignatura a ON a.asgid = mc.asgid
INNER JOIN area_academica aa ON aa.areaid = a.areaid
INNER JOIN curso c ON c.curid = mc.curid
INNER JOIN grado g ON g.graid = c.graid
INNER JOIN paralelo p ON p.prlid = c.prlid
INNER JOIN (
    SELECT
        pleid,
        graid,
        COUNT(*) AS total_paralelos
    FROM curso
    WHERE curestado = true
    GROUP BY pleid, graid
) paralelos ON paralelos.pleid = c.pleid
            AND paralelos.graid = c.graid;

CREATE VIEW vw_asistencia_resumen_diario AS
SELECT
    ae.estid,
    ca.pleid,
    ca.cafecha,
    CASE
        WHEN COUNT(*) FILTER (
            WHERE ae.aesestado IN ('ATRASO', 'FALTA_JUSTIFICADA', 'FALTA_INJUSTIFICADA')
        ) > 0
            THEN 'ALERTA'
        ELSE 'OK'
    END AS resumen_estado,
    COUNT(*) FILTER (WHERE ae.aesestado = 'ASISTENCIA') AS total_asistencias,
    COUNT(*) FILTER (WHERE ae.aesestado = 'ATRASO') AS total_atrasos,
    COUNT(*) FILTER (WHERE ae.aesestado = 'FALTA_JUSTIFICADA') AS total_faltas_justificadas,
    COUNT(*) FILTER (WHERE ae.aesestado = 'FALTA_INJUSTIFICADA') AS total_faltas_injustificadas
FROM asistencia_estudiante ae
INNER JOIN sesion_clase sc ON sc.sclid = ae.sclid
INNER JOIN calendario_asistencia ca ON ca.caid = sc.caid
WHERE sc.sclestado <> 'ANULADA'
GROUP BY ae.estid, ca.pleid, ca.cafecha;

