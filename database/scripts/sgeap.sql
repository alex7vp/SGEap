-- Estructura academica y asistencia.
--
-- Este script define las materias por curso, sus docentes, calendario de
-- asistencia, sesiones por hora, registros de asistencia y justificaciones.
-- No incluye el modulo de novedades.

CREATE TABLE area_academica (
    areaid integer GENERATED BY DEFAULT AS IDENTITY,
    areanombre varchar(100) NOT NULL,
    areaestado boolean NOT NULL DEFAULT true,
    areafecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    areafecha_modificacion timestamp,
    CONSTRAINT pk_area_academica PRIMARY KEY (areaid),
    CONSTRAINT uq_area_academica_areanombre UNIQUE (areanombre)
);

CREATE TABLE asignatura (
    asgid integer GENERATED BY DEFAULT AS IDENTITY,
    areaid integer NOT NULL,
    asgnombre varchar(100) NOT NULL,
    asgestado boolean NOT NULL DEFAULT true,
    asgfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    asgfecha_modificacion timestamp,
    CONSTRAINT pk_asignatura PRIMARY KEY (asgid),
    CONSTRAINT fk_asignatura_area_academica FOREIGN KEY (areaid)
        REFERENCES area_academica (areaid),
    CONSTRAINT uq_asignatura_areaid_asgnombre UNIQUE (areaid, asgnombre)
);

CREATE TABLE materia_curso (
    mtcid integer GENERATED BY DEFAULT AS IDENTITY,
    curid integer NOT NULL,
    asgid integer NOT NULL,
    mtcfecha_inicio date NOT NULL DEFAULT CURRENT_DATE,
    mtcfecha_fin date,
    mtcorden integer,
    mtcestado boolean NOT NULL DEFAULT true,
    mtcfecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    mtcfecha_modificacion timestamp,
    CONSTRAINT pk_materia_curso PRIMARY KEY (mtcid),
    CONSTRAINT fk_materia_curso_curso FOREIGN KEY (curid)
        REFERENCES curso (curid),
    CONSTRAINT fk_materia_curso_asignatura FOREIGN KEY (asgid)
        REFERENCES asignatura (asgid),
    CONSTRAINT ck_materia_curso_fechas
        CHECK (
            mtcfecha_fin IS NULL
            OR mtcfecha_fin >= mtcfecha_inicio
        )
);

CREATE UNIQUE INDEX uq_materia_curso_activa
    ON materia_curso (curid, asgid)
    WHERE mtcestado = true;

CREATE TABLE materia_curso_docente (
