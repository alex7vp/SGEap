            (cpagvalor_reportado IS NULL OR cpagvalor_reportado >= 0)
            AND (cpagvalor_aprobado IS NULL OR cpagvalor_aprobado >= 0)
        ),
    CONSTRAINT ck_cpag_archivo_tamano
        CHECK (cpagarchivo_tamano IS NULL OR cpagarchivo_tamano BETWEEN 1 AND 2097152),
    CONSTRAINT ck_cpag_archivo_extension
        CHECK (
            cpagarchivo_extension IS NULL
            OR cpagarchivo_extension IN ('PDF', 'JPG', 'JPEG', 'PNG')
        ),
    CONSTRAINT ck_cpag_representante_objetivo
        CHECK (
            cpagorigen <> 'REPRESENTANTE'
            OR (
                cobid_sugerido IS NOT NULL
                AND creid_sugerido IS NULL
                AND cpagarchivo_ruta IS NOT NULL
                AND cpagvalor_reportado IS NOT NULL
            )
        ),
    CONSTRAINT ck_cpag_revision
        CHECK (
            cpagestado = 'EN_REVISION'
            OR (
                usuid_revision IS NOT NULL
                AND cpagfecha_revision IS NOT NULL
            )
        ),
    CONSTRAINT ck_cpag_rechazo
        CHECK (
            cpagestado <> 'RECHAZADO'
            OR NULLIF(trim(cpagmotivo_rechazo), '') IS NOT NULL
        ),
    CONSTRAINT ck_cpag_aprobacion
        CHECK (
            cpagestado <> 'APROBADO'
            OR cpagvalor_aprobado IS NOT NULL
        ),
    CONSTRAINT ck_cpag_reverso
        CHECK (
            cpagestado <> 'REVERSADO'
            OR (
                usuid_reverso IS NOT NULL
                AND cpagfecha_reverso IS NOT NULL
                AND NULLIF(trim(cpagmotivo_reverso), '') IS NOT NULL
            )
        )
);

