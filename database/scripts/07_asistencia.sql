CREATE TABLE sesion_clase (
    sclid integer GENERATED BY DEFAULT AS IDENTITY,
    caid integer NOT NULL,
    mtcid integer NOT NULL,
    mcdid integer NOT NULL,
    sclnumero_hora integer NOT NULL,
    sclestado varchar(20) NOT NULL DEFAULT 'REGISTRADA',
    sclobservacion varchar(250),
    usuid_registro integer NOT NULL,
    sclfecha_registro timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    usuid_anulacion integer,
    sclfecha_anulacion timestamp,
    sclmotivo_anulacion varchar(250),
    CONSTRAINT pk_sesion_clase PRIMARY KEY (sclid),
    CONSTRAINT fk_sesion_clase_calendario FOREIGN KEY (caid)
        REFERENCES calendario_asistencia (caid),
    CONSTRAINT fk_sesion_clase_materia FOREIGN KEY (mtcid)
        REFERENCES materia_curso (mtcid),
    CONSTRAINT fk_sesion_clase_materia_docente FOREIGN KEY (mcdid, mtcid)
        REFERENCES materia_curso_docente (mcdid, mtcid),
    CONSTRAINT fk_sesion_clase_usuario_registro FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT fk_sesion_clase_usuario_anulacion FOREIGN KEY (usuid_anulacion)
        REFERENCES usuario (usuid),
    CONSTRAINT ck_sesion_clase_hora
        CHECK (sclnumero_hora BETWEEN 1 AND 7),
    CONSTRAINT ck_sesion_clase_estado
        CHECK (sclestado IN ('REGISTRADA', 'CERRADA', 'ANULADA')),
    CONSTRAINT ck_sesion_clase_anulacion
        CHECK (
            sclestado <> 'ANULADA'
            OR (
                usuid_anulacion IS NOT NULL
                AND sclfecha_anulacion IS NOT NULL
                AND NULLIF(trim(sclmotivo_anulacion), '') IS NOT NULL
            )
        )
);

CREATE UNIQUE INDEX uq_sesion_clase_materia_dia_hora_activa
    ON sesion_clase (mtcid, caid, sclnumero_hora)
    WHERE sclestado <> 'ANULADA';

