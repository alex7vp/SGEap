CREATE TABLE calendario_asistencia (
    caid integer GENERATED BY DEFAULT AS IDENTITY,
    pleid integer NOT NULL,
    cafecha date NOT NULL,
    catipo_jornada varchar(20) NOT NULL DEFAULT 'NORMAL',
    cahabilitado boolean NOT NULL DEFAULT true,
    cahora_limite integer,
    caobservacion varchar(250),
    usuid_registro integer NOT NULL,
    cafecha_creacion timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    cafecha_modificacion timestamp,
    CONSTRAINT pk_calendario_asistencia PRIMARY KEY (caid),
    CONSTRAINT fk_calendario_asistencia_periodo FOREIGN KEY (pleid)
        REFERENCES periodo_lectivo (pleid),
    CONSTRAINT fk_calendario_asistencia_usuario FOREIGN KEY (usuid_registro)
        REFERENCES usuario (usuid),
    CONSTRAINT uq_calendario_asistencia_pleid_fecha UNIQUE (pleid, cafecha),
    CONSTRAINT ck_calendario_asistencia_tipo
        CHECK (catipo_jornada IN ('NORMAL', 'REDUCIDA', 'SUSPENDIDA', 'ESPECIAL')),
    CONSTRAINT ck_calendario_asistencia_hora_limite
        CHECK (
            cahora_limite IS NULL
            OR cahora_limite BETWEEN 1 AND 7
        ),
    CONSTRAINT ck_calendario_asistencia_tipo_hora
        CHECK (
            (
                catipo_jornada = 'NORMAL'
                AND cahabilitado = true
                AND (cahora_limite IS NULL OR cahora_limite = 7)
            )
            OR (
                catipo_jornada = 'REDUCIDA'
                AND cahabilitado = true
                AND cahora_limite BETWEEN 1 AND 6
            )
            OR (
                catipo_jornada = 'SUSPENDIDA'
                AND cahabilitado = false
                AND cahora_limite IS NULL
            )
            OR (
                catipo_jornada = 'ESPECIAL'
                AND cahabilitado = true
                AND (cahora_limite IS NULL OR cahora_limite BETWEEN 1 AND 7)
            )
        )
);

